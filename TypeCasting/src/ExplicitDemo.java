public class ExplicitDemo {
    public static void main(String[] args) {
        // long to int
        long l= 10000000000000L;
        int i=(int) l;
        System.out.println(i);

        // double to int
        double d=1.23232323232323D;
        int i1=(int) d;
        System.out.println(i1);

        //int to char
        int b=931;
        char c=(char)b;
        System.out.println(c);
    }
}
