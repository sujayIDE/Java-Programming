public class ImplicitDemo {
    public static void main(String[] args) {
        //byte to int
      byte b=124;
      int x=b;
        System.out.println("byte to int conversion:" +x);

        //char to int
        char c='A';
        int a=c;
        System.out.println("char to int:"+a);

        //float to double
        float f=1.243434224F;
        double d=f;
        System.out.println("float to double:"+d);


    }
}
